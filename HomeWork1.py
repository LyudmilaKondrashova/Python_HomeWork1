# в которой находится эта точка (или на какой оси она находится).
x = int(input('Введите координату X точки:\n'))
y = int(input('Введите координату Y точки:\n'))
if (x == 0 and y == 0):
    print(f'Точка ({x},{y}) является началом координат')
elif (x == 0 and y != 0):
    print(f'Точка ({x},{y}) лежит на оси Oy')
elif (x != 0 and y == 0):
    print(f'Точка ({x},{y}) лежит на оси Ox')
elif (x > 0 and y > 0):
    print(f'Точка ({x},{y}) находится в первой четверти')
elif (x < 0 and y > 0):
    print(f'Точка ({x},{y}) находится во второй четверти')
elif (x < 0 and y < 0):
    print(f'Точка ({x},{y}) находится в третьей четверти')
else:
    print(f'Точка ({x},{y}) находится в четвертой четверти')


