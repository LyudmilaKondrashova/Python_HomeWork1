#1. Напишите программу, которая принимает на вход число и проверяет,
# кратно ли оно 5 и 10 или 15, но не 30.
N = int(input('Введите число\n'))
if ((N %5 == 0 and N %10 == 0) or (N %15 == 0)):
    if (N %30 != 0):
        print('Число кратно 5 и 10 или 15, но не кратно 30')
    else:
        print('Число кратно 5 и 10 или 15, и кратно 30')
else:
    print('Число не удовлетворяет условию')


