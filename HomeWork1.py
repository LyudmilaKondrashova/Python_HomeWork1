# ¬(X ⋁ Y ⋁ Z) = ¬X ⋀ ¬Y ⋀ ¬Z для всех значений предикат.
xx = bool(input('Введите X:\n'))
yy = bool(input('Введите Y:\n'))
zz = bool(input('Введите Z:\n'))
leftSide = not (xx or yy or zz)
rightSide = not xx and not yy and not zz
print(leftSide == rightSide)


#4. Напишите программу, которая принимает на вход координаты точки (X и Y),
