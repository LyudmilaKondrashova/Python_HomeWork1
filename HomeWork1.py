#6. Напишите программу, которая принимает на вход координаты двух точек
#и находит расстояние между ними в 2D пространстве
import math
x1 = int(input('Введите координату Х точки 1:\n'))
y1 = int(input('Введите координату Y точки 1:\n'))
x2 = int(input('Введите координату Х точки 2:\n'))
y2 = int(input('Введите координату Y точки 2:\n')) 
